CREATE TABLE ROBO(
                     ID INTEGER PRIMARY KEY AUTOINCREMENT,
                     BODY_ID INTEGER,
                     BODY_SIDE_ID INTEGER,
                     BODY_ITEM_ID INTEGER,
                     ACTION_ORDER INTEGER NOT NULL,
                     ACTION_ID INTEGER,
                     FOREIGN KEY (BODY_ID) REFERENCES CORPO(ID),
                     FOREIGN KEY (BODY_SIDE_ID) REFERENCES SIDE(ID),
                     FOREIGN KEY (BODY_ITEM_ID) REFERENCES BODY_ITEM(ID),
                     FOREIGN KEY (ACTION_ID) REFERENCES ACTION(ID)
);

CREATE INDEX ROBO_ID_IDX ON ROBO(ID);
CREATE INDEX ROBO_BODY_ID_IDX ON ROBO(BODY_ID);
CREATE INDEX ROBO_BODY_SIDE_ID_IDX ON ROBO(BODY_SIDE_ID);
CREATE INDEX ROBO_BODY_ITEM_ID_IDX ON ROBO(BODY_ITEM_ID);
CREATE INDEX ROBO_ACTION_ID_IDX ON ROBO(ACTION_ID);
CREATE INDEX ROBO_ORDER_IDX ON ROBO(ACTION_ORDER);
